import UIKit

///////////////////////////////////////////////////////////////////////////
/// @class ImmediatePanGestureRecognizer
/// @brief Classe qui permet de reconnaître un pan dès le premier tap
///
/// @author Mikael Ferland et Pierre To
/// @date 2017-10-12
///////////////////////////////////////////////////////////////////////////
class ImmediatePanGestureRecognizer: UIPanGestureRecognizer {
    
    override func touchesBegan(_ touches: Set<UITouch>, with event: UIEvent?) {
        super.touchesBegan(touches, with: event!)
        self.state = .began
        FacadeModele.instance.obtenirEtat().panGesture(sender: self)
    }
    
}

